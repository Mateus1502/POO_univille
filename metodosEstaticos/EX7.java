public class EX7 {
    public static void maiorMenor(int x,int y){
        if(x<y){
            System.out.println(x);
        }else{
            System.out.println(y);
        }
    }

    public static void main(String[] args) {
        int x=10;
        int y=5;
        maiorMenor(x,y);
    }
}
